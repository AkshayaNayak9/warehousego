    private void ADDBUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDBUTTONActionPerformed
        // TODO add your handling code here:
        try{
            String query= "insert into products values(?,?,?,?,?)";
            con=DriverManager.getConnection("jdbc:mysql://localhost:3308/projog","root","");
            PreparedStatement sp = con.prepareStatement(query);

            if(pidd.getText().isEmpty() || pname.getText().isEmpty() || category.getText().isEmpty()||quantity.getText().isEmpty()||price.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Enter the fields");
            }
            else{
                
                
                sp.setString(1,pidd.getText());
                sp.setString(2,pname.getText());
                sp.setString(3, category.getText());
                sp.setString(4, quantity.getText());
                sp.setString(5, price.getText());
                int i = sp.executeUpdate();
                if(i!=0)
                JOptionPane.showMessageDialog(null, "ADDED");
                else
                JOptionPane.showMessageDialog(null,"FAILED");
            }

            sp.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_ADDBUTTONActionPerformed


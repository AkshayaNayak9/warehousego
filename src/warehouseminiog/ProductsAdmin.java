        try{
            String query= "update  products set quantity=? where pid = ?";
            con=DriverManager.getConnection("jdbc:mysql://localhost:3308/projog","root","");
            PreparedStatement sp = con.prepareStatement(query);
            
             if(pidu.getText().isEmpty()|| qid.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Enter the field");
            } else{
                
                
                sp.setString(2,pidu.getText());
                sp.setString(1,qid.getText());
                int i = sp.executeUpdate();
                if(i!=0)
                JOptionPane.showMessageDialog(null, "Updated");
                else
                JOptionPane.showMessageDialog(null,"FAILED");
            }
             sp.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }

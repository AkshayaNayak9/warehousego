    private void ADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDActionPerformed
        // TODO add your handling code here:
        try{
            String query= "insert into supplier values(?,?,?,?)";
            con=DriverManager.getConnection("jdbc:mysql://localhost:3308/projog","root","");
            PreparedStatement sp = con.prepareStatement(query);

            if(sid.getText().isEmpty() || sname.getText().isEmpty() || cname.getText().isEmpty()||phno.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Enter the fields");
            }
            else{

                sp.setString(1,sid.getText());
                sp.setString(2,sname.getText());
                sp.setString(3, cname.getText());
                sp.setString(4,phno.getText());
                int i = sp.executeUpdate();
                if(i!=0)
                JOptionPane.showMessageDialog(null, "ADDED");
                else
                JOptionPane.showMessageDialog(null,"FAILED");
            }

            sp.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }

    }//GEN-LAST:event_ADDActionPerformed

